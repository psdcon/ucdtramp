    <h1>Trampoline Fitness</h1><br>
    We admit that trampolining has it's up's and downs but contrary to popular belief trampolining doesn't just involve bouncing around.<br><br>
    <center><img src="http://www.ucdtramp.com/images/pages/fitness1.jpg" id="planes" title="It involves being aeroplanes" alt="Flying"/></center><br>
  
    Fitness and core strength play a huge role in trampolining.<br><br>
    <img style="margin-left:1em;" src="http://www.ucdtramp.com/images/pages/fitness2.jpg" id="sass"  title="Doesnt that look like fun :)" alt="Flying some more"/>

    Remember when you were young bouncing on your trampoline with your
    friends seeing who could bounce higher and daring each other to try to do 
    cool new tricks? That is a big part of what goes on in the club and you 
    would not believe how much fun it is. You just have to be prepared for 
    being unexpectedly exhausted afterwards. <br><br>

    If you haven’t had enough exercise after 4 hours of bouncing a week, don't 
    worry. You can expect lots of body conditioning in our squad sessions. This 
    can include anything from push ups to sprints to learning how to handstand!
    Talk to Head Coach Ryan or Assistant Head Coach Colm at our regular 
    trainings for more info. <br><br>

    Cardiovascular fitness is also quite important, so running / jogging is very helpful. 
    But don't worry, we can make even the biggest couch potato into a fitness junkie in no time!
    Working out on a trampoline can improve your coordination and agility and 
    since your musculoskeletal system will be under slight stress, trampolining promotes stronger bones.<br><br>

    This is the sport for you if you want to work out in a fun, sociable, casual 
    way, and our qualified coaches will be glad to take you from your first
    bounce to your first somersault and beyond! One on one coaching means you'll 
    progress at your own rate, and within a few sessions you'll feel your fitness 
    and strength greatly improving. Keep on eye on the forum for extra training 
    and conditioning sessions! <br><br>

